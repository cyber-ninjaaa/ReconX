def section(title: str):
    width = 54
    print(f"\n{C}{BOLD}  в”Ң{'в”Җ' * width}в”җ")
    print(f"  в”Ӯ  {Y}{BOLD}{title:<{width - 2}}{C}в”Ӯ")
    print(f"  в””{'в”Җ' * width}в”ҳ{RESET}")


def info(msg: str):
    print(f"  {C}[{W}*{C}]{RESET} {msg}")


def success(msg: str):
    print(f"  {G}[{W}+{G}]{RESET} {msg}")


def warn(msg: str):
    print(f"  {Y}[{W}!{Y}]{RESET} {msg}")


def error(msg: str):
    print(f"  {R}[{W}вң—{R}]{RESET} {msg}")


def result(label: str, value: str):
    print(f"  {DIM}{label:<22}{RESET}{W}{value}{RESET}")


def found(value: str):
    print(f"  {G}  вҶі {value}{RESET}")


def summary_table(title: str, rows: list[tuple]):
    section(title)
    for label, value in rows:
        result(label, str(value))


